package cn.tim.contentresoverdemo;

public class StudentInfo {
    public int id;
    public String name;
    public int age;
    public String sex;

    public StudentInfo() {
    }

    public StudentInfo(int id, String name, int age, String sex) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.sex = sex;
    }
}
